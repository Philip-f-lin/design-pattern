package pattern.builder.demo1;

/**
 * 產品物件
 */
public class Bike {

    private String frame; // 車架

    private String seat; // 車座

    public String getFrame() {
        return frame;
    }

    public void setFrame(String frame) {
        this.frame = frame;
    }

    public String getSeat() {
        return seat;
    }

    public void setSeat(String seat) {
        this.seat = seat;
    }
}
